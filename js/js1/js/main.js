
function operar(tipo){
    //esto es una función

    var resultado = 0;

    var a = 5;

    var b = 8;

    if(tipo == 1){
        //sumaremos
        resultado = a + b;
        console.log("HEMOS SUMADO Y EL RESULTADO HA SIDO....",resultado);
        return;
    }

    if(tipo == 2){
        //restaremos
        resultado = a - b;
        console.log("HEMOS RESTADO Y EL RESULTADO HA SIDO....",resultado);
        return;
    }

    if(tipo == 3){
        //multiplicaremos
        resultado = a * b;
        console.log("HEMOS MULTIPLICADO Y EL RESULTADO HA SIDO....",resultado);
        return;
    }

    if(tipo == 4){
        //dividiremos
        resultado = a / b;
        console.log("HEMOS DIVIDIDO Y EL RESULTADO HA SIDO....",resultado);
        return;
    }

    return resultado;
}




